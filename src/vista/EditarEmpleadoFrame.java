    private void buscarbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarbtActionPerformed
        // TODO add your handling code here:
        System.out.println("Buscar EDITAREMPLEADOFRAME");
        em = ce.buscarEmpleado(cedulat.getText());
        if(em != null){
            JOptionPane.showMessageDialog(null, "El empleado se a encontrado");
            nombret.setText(em.getPerNombre());
            apellidot.setText(em.getPerApellido());
            cedulat1.setText(em.getPerCedula());
            convencionalt.setText(em.getPerTelConvencional());
            celulart.setText(em.getPerTelCelular());
            ubicaciont.setText(em.getPerDireccion());
            correot.setText(em.getPerCorreo());
            usernamet.setText(em.getEmpUsername());
            paswort1.setText(em.getEmpPassword());
            paswort2.setText(em.getEmpPassword());
        }else{
            JOptionPane.showMessageDialog(null, "El empleado no se a encontrado");
        }
    }//GEN-LAST:event_buscarbtActionPerformed

    private void editarbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarbtActionPerformed
        // TODO add your handling code here:
        System.out.println("Editar EDITAREMPLEADOFRAME");
        if(paswort1.getText().equalsIgnoreCase(paswort2.getText())==true && em != null){
            em.setPerNombre(nombret.getText());
            em.setPerApellido(apellidot.getText());
            em.setPerCedula(cedulat1.getText());
            em.setPerTelConvencional(convencionalt.getText());
            em.setPerTelCelular(celulart.getText());
            em.setPerDireccion(ubicaciont.getText());
            em.setPerCorreo(correot.getText());
            em.setEmpUsername(usernamet.getText());
            em.setEmpPassword(paswort2.getText());
            Sucursal suc = cs.buscarSucursal((String)sucursalcombox.getSelectedItem());
            if(ce.editarEmpleado(em, suc)==true){
                JOptionPane.showMessageDialog(null, "El empleado se a editado con exito");
                cedulat.setText("");
                cedulat1.setText("");
                nombret.setText("");
                apellidot.setText("");
                convencionalt.setText("");
                celulart.setText("");
                ubicaciont.setText("");
                correot.setText("");
                usernamet.setText("");
                paswort1.setText("");
                paswort2.setText("");
                ce.listarEmpleados();
            }else{
               JOptionPane.showMessageDialog(null, "El empleado no pudo ser editado"); 
            }
        }else{
            JOptionPane.showMessageDialog(null, "Las contrasenas no son iguales o el Administrador no existe");
        }
    }//GEN-LAST:event_editarbtActionPerformed

