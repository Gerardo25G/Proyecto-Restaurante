    private void buscarbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarbtActionPerformed
        // TODO add your handling code here:
        System.out.println("Buscar Administrador");
        adm = ca.buscarAdministrador(cedulat.getText());
        if(adm != null){
            JOptionPane.showMessageDialog(null, "El administrador se a encontrado");
            nombret.setText(adm.getPerNombre());
            apellidot.setText(adm.getPerApellido());
            cedulat1.setText(adm.getPerCedula());
            convencionalt.setText(adm.getPerTelConvencional());
            celulart.setText(adm.getPerTelCelular());
            ubicaciont.setText(adm.getPerDireccion());
            correot.setText(adm.getPerCorreo());
            usernamet.setText(adm.getAdmUsername());
            paswort1.setText(adm.getAdmPasword());
            paswort2.setText(adm.getAdmPasword());
        }else{
            JOptionPane.showMessageDialog(null, "El administrador no se a encontrado");
        }
    }//GEN-LAST:event_buscarbtActionPerformed

    private void editarbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarbtActionPerformed
        // TODO add your handling code here:
        if(paswort1.getText().equalsIgnoreCase(paswort2.getText())==true && adm != null){
            adm.setPerNombre(nombret.getText());
            adm.setPerApellido(apellidot.getText());
            adm.setPerCedula(cedulat1.getText());
            adm.setPerTelConvencional(convencionalt.getText());
            adm.setPerTelCelular(celulart.getText());
            adm.setPerDireccion(ubicaciont.getText());
            adm.setPerCorreo(correot.getText());
            adm.setAdmUsername(usernamet.getText());
            adm.setAdmPasword(paswort2.getText());
            Sucursal suc = sc.buscarSucursal((String)sucursalcombox.getSelectedItem());
            if(ca.editarAdministrador(adm, suc)==true){
                JOptionPane.showMessageDialog(null, "El administrador se a editado con exito");
                cedulat.setText("");
                cedulat1.setText("");
                nombret.setText("");
                apellidot.setText("");
                convencionalt.setText("");
                celulart.setText("");
                ubicaciont.setText("");
                correot.setText("");
                usernamet.setText("");
                paswort1.setText("");
                paswort2.setText("");
                ca.listarAdministradores();
            }else{
               JOptionPane.showMessageDialog(null, "El administrador no pudo ser editado"); 
            }
        }else{
            JOptionPane.showMessageDialog(null, "Las contrasenas no son iguales o el Administrador no existe");
        }
        
    }//GEN-LAST:event_editarbtActionPerformed

