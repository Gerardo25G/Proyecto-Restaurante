    private void buscarbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarbtActionPerformed
        // TODO add your handling code here:
        System.out.println("buscar BUSCARBODEGAFRAME");
        b = cb.buscarBodega(nombret.getText()+(String)sucursalcombox.getSelectedItem());
        if(b != null){
            JOptionPane.showMessageDialog(null, "La bodega se encuentra registrada. \n"
                    + "Datos: \n"
                    + "Nombre: "+b.getNombre()+"\n"
                    + "Producto: "+b.getDetalleBodega().get(0).getProducto().getNombre()+"\n"
                    + "Cantidad: "+b.getDetalleBodega().get(0).getCantidadTotal());
        }else{
            JOptionPane.showMessageDialog(null, "Bodega no registrada");
        }
    }//GEN-LAST:event_buscarbtActionPerformed

